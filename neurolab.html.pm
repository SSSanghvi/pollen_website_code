		◊h1{Neurolab Logo}
		
		◊p{
			I worked with the people over at NeuroLab, a student organization at UC Berkeley, 
			to create a new logo in March 2018. The logo is analogous to the club's mixed 
			focus between Neurology and Electrical Engineering. In order to illustrate how 
			the club brings these two fields together, I came up with a design where the two 
			lobes of a brain are connected with a capacitor - which can be interpreted as 
			providing power to the mind. ​
		}

